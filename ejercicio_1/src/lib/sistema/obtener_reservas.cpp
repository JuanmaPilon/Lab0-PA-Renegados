DtReserva** Sistema::obtenerReserva (
	int numero,
	int&   cantidad
){
	Habitacion* const habitacion = Buscar_Habitacion( numero );

	if( habitacion == nullptr )
		throw std::invalid_argument( "Habitacion no encontrado" );
	else
		return habitacion->Reservas( cantidad );
}

